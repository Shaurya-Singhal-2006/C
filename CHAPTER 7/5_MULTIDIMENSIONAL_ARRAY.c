//                   MULTIDIMENSIONAL ARRAY 

// 2 D array 


#include<stdio.h>
int main(){

    int arr[2][2] = {{1,2},{3,4}};   //declare

    // access
    //                         we have to visulize the multidimensional array in matrix form
    printf("%d\n",arr[0][0]);  // 1
    printf("%d\n",arr[0][1]);  // 2
    printf("%d\n",arr[1][0]);  // 3
    printf("%d\n",arr[1][1]);  // 4

    return 0;
}
